fn main() {

    // Scalar Type: Int, Float, Boolean
    // Unsinged - can never be negative - have to be declared
    // u8, u16, u32, u64, u128, usize
    // signed - can be positive and negative
    // i8, i16, i32, i64, i128, isize
    println!("Max Size {}", i32::MAX);
    println!("Max Size {}", i64::MAX);
    println!("Max Size {}", u32::MAX);
    println!("Max Size {}", u64::MAX);

    // Max Size 2147483647
    // Max Size 9223372036854775807
    // Max Size 4294967295
    // Max Size 18446744073709551615

}
